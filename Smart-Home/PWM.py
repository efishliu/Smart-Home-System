'''***********************************************
*模块： 数字调光模块
*主要功能： 根据光照强度进行自动LED调光
***********************************************'''
import binascii

'''***********************************************
*函数： led_light
*相关参数： num 亮度调节值
*主要功能： 根据num亮度值进行LED调光     
***********************************************'''
def led_light(ser,num):
    senddata='ccee010a01'+num+'000000000000000000ff'
    senddata=binascii.b2a_hex(senddata)
    ser.write(senddata)